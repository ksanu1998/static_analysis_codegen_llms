# Find the sum of the first N Centered Decagonal Numbers. 
def Centered_decagonal_num(n): 
    return (n*(n+1))/2

# Find the sum of the first N Centered Hexagonal Numbers. 
def Centered_hexagonal_num(n): 
    return (n*(2*n-1))/2

# Find the sum of the first N Centered Octagonal Numbers. 
def Centered_octagonal_num(n): 
    return (n*(3*n-1))/2

# Find the sum of the first N Centered Triangular Numbers. 
def Centered_triangular_num(n): 
    return (n*(n+1))/2