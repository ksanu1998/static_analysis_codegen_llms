# Count of distinct index pair ( i , j ) such that element sum of First Array is greater. 
from bisect import bisect_left


def numberOfPairs(a, b, n): 
    # Sort the array 
    a.sort() 
    b.sort() 

    # Initialize result 
    count = 0

    # Traverse through the array 
    for i in range(n): 
        # Find the index of the element in the second array 
        j = bisect_left(b, a[i]) 

        # If the element is found 
        if j!= len(b): 
            # Increment the count 
            count += len(b) - j 

    return count 


