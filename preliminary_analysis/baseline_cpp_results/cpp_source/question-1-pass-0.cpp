// Check if a number can be represented as sum of two positive perfect cubes. 
 #include <bits/stdc++.h> 
 using namespace std ;
 bool sumOfTwoCubes ( int n ) {
 
    int i = 1, j = n;
    while (i <= j) {
        int a = i * i * i;
        int b = j * j * j;
        if (a + b == n)
            return true;
        else if (a + b > n)
            j--;
        else
            i++;
    }
    return false;
}
int main() {
    int n;
    cin >> n;
    if (sumOfTwoCubes(n))
        cout << "Yes";
    else
        cout << "No";
    return 0;
}



























































































































































































































































































































