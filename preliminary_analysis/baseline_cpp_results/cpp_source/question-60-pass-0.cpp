// Check if count of even divisors of N is equal to count of odd divisors. 
 #include <bits/stdc++.h> 
 using namespace std ;
 bool divisorsSame ( int n ) {
 
    int countEven = 0, countOdd = 0;
    for ( int i = 1; i <= sqrt ( n ); i++ ) {
        if ( n % i == 0 ) {
            if ( n / i == i )
                countEven++;
            else {
                countEven++;
                countOdd++;
            }
        }
    }
    return ( countEven == countOdd );
}
int main () {
    int n;
    cin >> n;
    if ( divisorsSame ( n ) )
        cout << "YES\n";
    else
        cout << "NO\n";
    return 0;
}










































































































































































































































































































