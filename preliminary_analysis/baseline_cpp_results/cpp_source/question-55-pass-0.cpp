// Program to check if N is a Centered Cubic Number. 
 #include <bits/stdc++.h> 
 using namespace std ;
 bool isCenteredcube ( int N ) {
 
    int x = sqrt ( N );
    if ( x * x * x!= N )
        return false;
    int y = sqrt ( x );
    if ( y * y * y!= x )
        return false;
    int z = sqrt ( y );
    if ( z * z * z!= y )
        return false;
    return true;
}

int main () {
    int N ;
    cin >> N ;
    if ( isCenteredcube ( N ) )
        cout << "Yes" << endl;
    else
        cout << "No" << endl;
    return 0;
}



























































































































































































































































































































