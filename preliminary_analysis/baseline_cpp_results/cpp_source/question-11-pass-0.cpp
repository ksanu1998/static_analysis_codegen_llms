// Minimum number of coins having value equal to powers of 2 required to obtain N. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void count_setbit ( int N ) {
 
    int count = 0 ;
    while ( N ) {
        N = N & ( N - 1 ) ;
        count++ ;
    }
    cout << "Minimum number of coins required to obtain " << N << " is " << count << endl ;
 }
 
 int main ( ) {
    int N ;
    cout << "Enter the value of N : " ;
    cin >> N ;
    count_setbit ( N ) ;
    return 0 ;
 }
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 