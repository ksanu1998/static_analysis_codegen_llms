// Minimum decrements to make integer A divisible by integer B. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void movesRequired ( int a , int b ) {
 
    int count = 0 ;
    while ( a % b!= 0 ) {
        a = a - 1 ;
        count++ ;
    }
    cout << count << endl ;
 }
 int main ( ) {
    int a, b ;
    cin >> a >> b ;
    movesRequired ( a, b ) ;
    return 0 ;
 }

























































































































































































































































































































































































