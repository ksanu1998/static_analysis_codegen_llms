// Evaluate the expression ( N1 * ( N. 
 #include <bits/stdc++.h> 
 using namespace std ;
 #define mod  1000000007 
 int ValOfTheExpression ( int n ) {
 
    int ans = 1;
    while ( n > 0 ) {
        int digit = n % 10;
        ans = ( ans * digit ) % mod;
        n /= 10;
    }
    return ans;
}
int main() {
    int n;
    cin >> n;
    cout << ValOfTheExpression ( n ) << endl;
    return 0;
}
































































































































































































































































































































































