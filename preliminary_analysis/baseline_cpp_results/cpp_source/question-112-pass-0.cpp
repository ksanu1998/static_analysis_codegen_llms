// Minimum operations required to make two numbers equal. 
 #include <bits/stdc++.h> 
 using namespace std ;
 long long int minOperations ( long long int A , long long int B ) {
 
    long long int count = 0 ;
    while ( A!= B ) {
        if ( A > B ) {
            A = A - B ;
            count += A ;
        }
        else {
            B = B - A ;
            count += B ;
        }
    }
    return count ;
}

int main() {
    long long int A, B ;
    cin >> A >> B ;
    cout << minOperations ( A, B ) << endl ;
    return 0 ;
}


















































































































































































































































































































































