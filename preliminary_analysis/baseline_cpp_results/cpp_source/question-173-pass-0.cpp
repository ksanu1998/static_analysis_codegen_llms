// Find the height of a right. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int getHeight ( int X ) {
 
     int height = 0 ;
     while ( X > 0 ) {
         height++ ;
         X = X & ( X - 1 ) ;
     }
     return height ;
 }
 
