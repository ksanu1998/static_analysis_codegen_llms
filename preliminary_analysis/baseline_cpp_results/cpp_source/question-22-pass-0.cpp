// Generate first K multiples of N using Bitwise operators. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void Kmultiples ( int n , int k ) {
 
    int i = 0 ;
    while ( i < k ) {
        cout << n << " " ;
        n = n << 1 ;
        i++ ;
    }
 }
 int main ( ) {
    int n, k ;
    cin >> n >> k ;
    Kmultiples ( n, k ) ;
    return 0 ;
 }


























































































































































































































































































































































































