// Check if an integer is rotation of another given integer. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int check ( int A , int B ) {
 
     int x = A ;
     int y = B ;
     while ( x!= 0 ) {
         int rem = y % x ;
         y = x ;
         x = rem ;
     }
     return ( y == 1 ) ;
 }
 int main ( ) {
     int A = 10 ;
     int B = 20 ;
     cout << check ( A, B ) << endl ;
     return 0 ;
 }
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
