# Check if a number can be represented as sum of two positive perfect cubes. 
import math


def sumOfTwoCubes(n): 
    for i in range(1, int(math.sqrt(n)) + 1): 
        j = int(math.sqrt(n - i*i)) 
        if (i*i + j*j == n): 
            return True
    return False


