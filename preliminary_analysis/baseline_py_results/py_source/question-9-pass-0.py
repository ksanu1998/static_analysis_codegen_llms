# Count Non. 
import sys


def gcd(a, b): 
    if a == 0: 
        return b 
    return gcd(b % a, a) 


def lcm(a, b): 
    return (a * b) // gcd(a, b) 


def main():
    n = int(input())
    arr = list(map(int, input().split()))
    arr.sort()
    ans = 1
    for i in range(n):
        ans = lcm(ans, arr[i])
    print(ans)


if __name__ == '__main__':
    main()






























































































































































































































































































































































