# Find two numbers whose difference of fourth power is equal to N. 
from math import pow, ceil


def solve(n): 
    for i in range(1, n): 
        for j in range(i, n): 
            if pow(i, 4) - pow(j, 4) == n: 
                return i, j 


if __name__ == '__main__': 
    n = int(input()) 
    print(solve(n)) 









































































































































































































































































































































































































