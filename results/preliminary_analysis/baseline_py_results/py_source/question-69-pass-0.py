# Find the sum of the first N Centered Triangular Number. 
def center_Triangular_num(n): 
    return (n * (n + 1)) / 2 
  
# Driver Code 
n = 10
print("Sum of the first", n, "Centered Triangular Number is", 
      center_Triangular_num(n)) 
  