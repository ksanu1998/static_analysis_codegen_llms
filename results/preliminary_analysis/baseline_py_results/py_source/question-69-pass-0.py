# Find the sum of the first N Centered Octagonal Number. 
def center_Octagonal_num(n): 
    return (n * (n * 3 - 3)) / 2 
  
# Driver Code 
n = 10
print("Sum of the first", n, "Centered Octagonal Number is", 
      center_Octagonal_num(n)) 
  
