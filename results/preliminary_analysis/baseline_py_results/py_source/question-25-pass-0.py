# Find temperature of missing days using given sum and average. 
def findTemperature(x, y, s): 
    return (s - x) / (y - x) 
  
# Driver Code 
if __name__ == '__main__': 
    # Given values 
    x = 10 
    y = 20 
    s = 30 
  
    # Function call 
    print("Temperature on missing days is", 
          findTemperature(x, y, s)) 
  
