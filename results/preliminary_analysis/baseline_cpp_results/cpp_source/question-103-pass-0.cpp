// Pythagorean Triplet with given sum using single loop. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void PythagoreanTriplet ( int n ) {
 
    int a, b, c ;
    for ( a = 1 ; a <= n ; a++ ) {
        for ( b = a + 1 ; b <= n ; b++ ) {
            c = n - a - b ;
            if ( a * a + b * b == c * c ) {
                cout << "Pythagorean Triplet is : " << a << " " << b << " " << c << endl ;
                return ;
            }
        }
    }
    cout << "No Pythagorean Triplet with given sum " << n << endl ;
 }
 
 int main ( ) {
    int n ;
    cout << "Enter the sum of the triplet : " ;
    cin >> n ;
    PythagoreanTriplet ( n ) ;
    return 0 ;
 }






































































































































































































































































