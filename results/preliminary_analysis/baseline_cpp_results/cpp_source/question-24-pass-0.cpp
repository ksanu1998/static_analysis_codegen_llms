// Count of repeating digits in a given Number. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int countRepeatingDigits ( int N ) {
 
    int count = 0;
    int temp = N;
    while (temp > 0) {
        int digit = temp % 10;
        temp /= 10;
        if (digit == temp % 10)
            count++;
    }
    return count;
}

int main() {
    int N;
    cin >> N;
    cout << countRepeatingDigits(N);
    return 0;
}






































































































































































































































































































































































