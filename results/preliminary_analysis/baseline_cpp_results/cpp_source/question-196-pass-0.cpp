// Count all prefixes of the given binary array which are divisible by x. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int CntDivbyX ( int arr [ ] , int n , int x ) {
 
 int prefix [ n + 1 ] ;
 int i ;
 
 // Initialize prefix array
 for ( i = 0 ; i < n ; i++ )
  prefix [ i + 1 ] = arr [ i ] ;
 
 // Initialize result
 int res = 0 ;
 
 // Traverse through all prefixes
 for ( i = 0 ; i < n ; i++ ) {
  if ( prefix [ i ] % x == 0 )
   res++ ;
 }
 
 return res ;
}

int main() {
    int n, x;
    cin>>n>>x;
    int arr[n];
    for(int i=0;i<n;i++)
        cin>>arr[i];
    cout<<CntDivbyX(arr,n,x);
    return 0;
}



































































































































































































































































