// Program to determine the Quadrant of a Complex number. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void quadrant ( string s ) {
 
    int n = s.length() ;
    int i = 0 ;
    int j = n-1 ;
    int count = 0 ;
    while ( i < j ) {
        if ( s[i]!= s[j] ) {
            count++ ;
        }
        i++ ;
        j-- ;
    }
    if ( count == 0 ) {
        cout << "The number is a palindrome" << endl ;
    }
    else {
        cout << "The number is not a palindrome" << endl ;
    }
 }
 int main ( ) {
    string s ;
    cout << "Enter a string : " ;
    cin >> s ;
    quadrant ( s ) ;
    return 0 ;
 }

































































































































































































































































































