// Fermat 's Factorization Method. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void FermatFactors ( int n ) {
 
    int a = 2 ;
    int d = n ;
    while ( d % a!= 0 ) {
        a = a + 1 ;
    }
    cout << "Factor of " << n << " is " << a << endl ;
    d = d / a ;
    while ( d % a!= 0 ) {
        a = a + 1 ;
    }
    cout << "Factor of " << n << " is " << a << endl ;
    d = d / a ;
    cout << "Factor of " << n << " is " << d << endl ;
 }
 
 int main () {
    int n ;
    cout << "Enter a number : " ;
    cin >> n ;
    FermatFactors ( n ) ;
    return 0 ;
 }






















































































































































































































































































