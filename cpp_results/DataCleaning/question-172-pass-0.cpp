// Count total set bits in all numbers from 1 to n. 
 #include <iostream> 
 using namespace std ;
 int countSetBits ( int n ) {
 
 int count = 0 ;
 while ( n ) {
 
 count += n & 1 ;
 n >>= 1 ;
 }
 return count ;
 }
 
 int main () {
 
 int n = 10 ;
 cout << "Total set bits in numbers from 1 to "
 << n << " is " << countSetBits ( n ) ;
 return 0 ;
 }



























































































































































































































































































































































































