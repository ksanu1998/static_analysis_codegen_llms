// Ways to choose balls such that at least one ball is chosen. 
 #include <bits/stdc++.h> 
 using namespace std ;
 const int MOD = 1000000007 ;
 int countWays ( int n ) {
 
    // Base case
    if ( n == 0 ) return 1 ;
 
    // Recursive case
    int ways = 0 ;
    for ( int i = 1 ; i <= n ; i++ ) {
        ways = ( ways + countWays ( n - i ) ) % MOD ;
    }
    return ways ;
 }
 
 int main () {
    int n ;
    cin >> n ;
    cout << countWays ( n ) << endl ;
    return 0 ;
 }






































































































































































































































































































































