// Product of N terms of a given Geometric series. 
 #include <bits/stdc++.h> 
 using namespace std ;
 float productOfGP ( float a , float r , int n ) {
 
    float p = 1 ;
    for ( int i = 0 ; i < n ; i++ ) {
        p = p * ( a / ( 1 - r ) ) ;
    }
    return p ;
 }
 
 int main () {
 
    float a, r ;
    int n ;
    cout << "Enter the value of a, r and n : " ;
    cin >> a >> r >> n ;
    cout << "The product of " << n << " terms of a Geometric series is : " << productOfGP ( a, r, n ) ;
    return 0 ;
 }































































































































































































































































































































