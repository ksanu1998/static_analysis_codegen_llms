// Count quadruplets ( A , B , C , D ) till N such that sum of square of A and B is equal to that of C and D. 
 #include <bits/stdc++.h> 
 using namespace std ;
 typedef long long int ll ;
 ll countQuadraples ( ll N ) {
 
    ll count = 0 ;
    for ( ll i = 1 ; i <= N ; i++ ) {
        for ( ll j = i + 1 ; j <= N ; j++ ) {
            for ( ll k = j + 1 ; k <= N ; k++ ) {
                for ( ll l = k + 1 ; l <= N ; l++ ) {
                    if ( i * i + j * j == k * k + l * l ) {
                        count++ ;
                    }
                }
            }
        }
    }
    return count ;
}

int main ( ) {
    ll N ;
    cin >> N ;
    cout << countQuadraples ( N ) << endl ;
    return 0 ;
}


































































































































































































































































