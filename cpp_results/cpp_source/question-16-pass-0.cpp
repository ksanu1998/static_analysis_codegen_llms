// Sum of first N natural numbers with alternate signs. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int alternatingSumOfFirst_N ( int N ) {
 
    int sum = 0 ;
    for ( int i = 1 ; i <= N ; i++ ) {
        if ( i % 2 == 0 ) {
            sum += i ;
        }
        else {
            sum -= i ;
        }
    }
    return sum ;
 }
 int main ( ) {
    int N ;
    cin >> N ;
    cout << alternatingSumOfFirst_N ( N ) << endl ;
    return 0 ;
 }
































































































































































































































































































































































