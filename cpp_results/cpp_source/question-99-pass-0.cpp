// Program to print numbers from N to 1 in reverse order. 
 #include <bits/stdc++.h> 
 using namespace std ;
 void PrintReverseOrder ( int N ) {
 
     if ( N > 0 ) {
         PrintReverseOrder ( N - 1 ) ;
         cout << N << " " ;
     }
 }
 int main ( ) {
     int N ;
     cout << "Enter a number : " ;
     cin >> N ;
     PrintReverseOrder ( N ) ;
     return 0 ;
 }






























































































































































































































































































































































































