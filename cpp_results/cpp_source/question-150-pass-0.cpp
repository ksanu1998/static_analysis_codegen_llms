// Reduce N to 1 with minimum number of given operations. 
 #include <bits/stdc++.h> 
 using namespace std ;
 long long int minOperations ( long long int n ) {
 
    long long int count = 0;
    while (n > 1) {
        if (n % 2 == 0) {
            n /= 2;
            count++;
        }
        else {
            n = (n * 3 + 1) / 2;
            count++;
        }
    }
    return count;
}

int main() {
    long long int n;
    cin >> n;
    cout << minOperations(n);
    return 0;
}



















































































































































































































































































































































