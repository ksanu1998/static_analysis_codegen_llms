// Largest number less than or equal to Z that leaves a remainder X when divided by Y. 
 #include <bits/stdc++.h> 
 using namespace std ;
 int get ( int x , int y , int z ) {
 
     int ans = 0 ;
     while ( z >= x ) {
         z = z - x ;
         ans++ ;
     }
     return ans ;
 }
