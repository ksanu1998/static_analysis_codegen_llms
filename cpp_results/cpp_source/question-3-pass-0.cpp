// Nth natural number after removing all numbers consisting of the digit 9. 
 #include <bits/stdc++.h> 
 using namespace std ;
 long long findNthNumber ( long long N ) {
 
    long long count = 0 ;
    long long number = 1 ;
    while ( count < N ) {
        if ( number % 10!= 9 ) {
            count++ ;
        }
        number++ ;
    }
    return number ;
}

int main() {
    long long N ;
    cin >> N ;
    cout << findNthNumber ( N ) << endl ;
    return 0 ;
}





































































































































































































































































































































































