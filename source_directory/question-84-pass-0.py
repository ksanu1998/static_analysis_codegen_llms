# Find the Kth number which is not divisible by N. 
import sys


def kthNonDivisible(N, K): 
    # Base Case 
    if K == 1: 
        return 1

    # Find the Kth number which is not divisible by N. 
    # The number is (Kth number - 1) / N + 1 
    return (kthNonDivisible(N, K - 1) - 1) / N + 1


