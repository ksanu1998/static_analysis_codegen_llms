# Smallest N digit number divisible by N. 
import math


def smallestNumber(N): 
    # Base case 
    if N == 1: 
        return 0
  
    # Find the largest number that is 
    # divisible by N 
    largest = math.ceil(N * (N - 1) / 2) 
  
    # Find the smallest number that is 
    # divisible by N 
    smallest = math.floor(N * (N + 1) / 2) 
  
    # Return the difference 
    return largest - smallest 
  
# Driver code 
N = 10
print(smallestNumber(N)) 

