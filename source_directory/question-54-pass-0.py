# Maximize sum of minimum difference of divisors of nodes in N. 
import math
sub = [0 for i in range(100005)]


def minDivisorDifference(n): 
    if (n == 1): 
        return 0
    if (sub[n]!= 0): 
        return sub[n] 
    minDiff = math.inf
    for i in range(1, n): 
        if (n % i == 0): 
            minDiff = min(minDiff, minDivisorDifference(i) + minDivisorDifference(n // i)) 
    sub[n] = minDiff 
    return minDiff 


def main():
    n = int(input())
    print(minDivisorDifference(n))


if __name__ == '__main__':
    main()













































































































































































































































































































