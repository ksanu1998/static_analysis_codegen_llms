# Maximum Prefix Sum possible by merging two given arrays. 
def maxPresum(a, b): 
    n = len(a) 
    m = len(b) 
    res = 0
    for i in range(n): 
        for j in range(m): 
            res = max(res, a[i] + b[j]) 
    return res 
  
# Driver Code 
a = [1, 2, 3, 4, 5] 
b = [1, 2, 3, 4, 5] 
print("Maximum Prefix Sum possible by merging two given arrays is", 
      maxPresum(a, b)) 
  
