# Check if count of even divisors of N is equal to count of odd divisors. 
import math


def divisorsSame(n): 
    count = 0
    for i in range(1, int(math.sqrt(n)) + 1): 
        if n % i == 0: 
            count += 2
    return count % 2 == 0


if __name__ == '__main__': 
    n = int(input())
    if divisorsSame(n): 
        print("YES")
    else: 
        print("NO")



















































































































































































































































































































































































