# Find the sum of the first Nth Centered Pentadecagonal Number. 
def Centered_Pentadecagonal_num(n): 
    return (((3 * n * (n + 1)) - 1) / 2) 
  
# Driver Code 
n = 10
print("Sum of the first", n, "Centered Pentadecagonal Numbers is", 
      Centered_Pentadecagonal_num(n)) 
  
